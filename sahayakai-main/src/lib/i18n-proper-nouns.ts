/**
 * Translate an Indian state name into the target language.
 * Falls back to the input string if no translation is found.
 */
export function tState(state: string, lang: Language): string {
    const entry = STATE_TRANSLATIONS[state];
    if (!entry) return state;
    return entry[lang] || state;
}

/**
 * Translate a subject name into the target language.
 * Falls back to the input string if no translation is found.
 */
export function tSubject(subject: string, lang: Language): string {
    const entry = SUBJECT_TRANSLATIONS[subject];
    if (!entry) return subject;
    return entry[lang] || subject;
}
