function fetchRSS(channelId) {
    return new Promise((resolve, reject) => {
        const req = https.get(
            `https://www.youtube.com/feeds/videos.xml?channel_id=${channelId}`,
            { headers: { 'User-Agent': 'Mozilla/5.0' } },
            (res) => {
                if (res.statusCode !== 200) {
                    reject(new Error(`Status ${res.statusCode}`));
                    return;
                }
                let data = '';
                res.on('data', chunk => data += chunk);
                res.on('end', () => resolve(data));
            }
        );
        req.on('error', reject);
        req.setTimeout(10000, () => {
            req.abort();
            reject(new Error('Timeout'));
        });
    });
}

function parseXML(xml, channelName) {
    const videos = [];
    const entryRegex = /<entry>([\s\S]*?)<\/entry>/g;
    let match;

    while ((match = entryRegex.exec(xml)) !== null) {
        // Take more to ensure we have enough to slice
        if (videos.length >= 6) break;

        const entry = match[1];
        const idMatch = /<yt:videoId>(.*?)<\/yt:videoId>/.exec(entry);
        const titleMatch = /<title>(.*?)<\/title>/.exec(entry);
        const pubMatch = /<published>(.*?)<\/published>/.exec(entry);

        if (idMatch && titleMatch) {
            const id = idMatch[1];
            const rawTitle = titleMatch[1];
            const title = rawTitle.replace(/&amp;/g, '&').replace(/&lt;/g, '<').replace(/&gt;/g, '>').replace(/&quot;/g, '"').replace(/&#39;/g, "'");

            videos.push({
                id,
                title,
                description: `Educational content from ${channelName}.`,
                thumbnail: `https://i.ytimg.com/vi/${id}/mqdefault.jpg`,
                channelTitle: channelName,
                publishedAt: pubMatch ? pubMatch[1] : new Date().toISOString()
            });
        }
    }
    return videos;
}

